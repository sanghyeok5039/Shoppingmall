package com.shoppingmall.dto;

public class ClosingDto {
	String clos_date;
	String clos_total;
	String clos_ords_count;
	
	public String getClos_date() {
		return clos_date;
	}
	public void setClos_date(String clos_date) {
		this.clos_date = clos_date;
	}
	public String getClos_total() {
		return clos_total;
	}
	public void setClos_total(String clos_total) {
		this.clos_total = clos_total;
	}
	public String getClos_ords_count() {
		return clos_ords_count;
	}
	public void setClos_ords_count(String clos_ords_count) {
		this.clos_ords_count = clos_ords_count;
	}
	
	
}
